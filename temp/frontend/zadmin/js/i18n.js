  const STORAGE_KEY = 'llmui_lang';
  const FALLBACK = 'fr';
  let _lang = localStorage.getItem(STORAGE_KEY) || FALLBACK;

  /**
   * Résout un chemin pointé (ex: "git.create") dans l'objet de traductions.
   * @param {string} key  - Chemin "section.clé" ou "section.sous.clé"
   * @param {string} lang - Code langue (optionnel, défaut: langue courante)
   * @returns {string}
   */
  function t(key, lang) {
    const locale = LOCALES[lang || _lang] || LOCALES[FALLBACK];
    const parts = key.split('.');
    let val = locale;
    for (const p of parts) {
      if (val == null) break;
      val = val[p];
    }
    if (typeof val === 'string') return val;
    // Repli sur français
    if ((lang || _lang) !== FALLBACK) return t(key, FALLBACK);
    return key; // clé brute si introuvable
  }

  /**
   * Change la langue active et sauvegarde dans localStorage.
   * @param {string} lang - Code langue (fr | en | es | de | pt | ar)
   */
  function setLang(lang) {
    if (!LOCALES[lang]) return;
    _lang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    applyAll();
  }

  /** Retourne la langue active. */
  function getLang() { return _lang; }

  /**
   * Met à jour tous les éléments DOM portant data-i18n="section.clé".
   * Supporte aussi data-i18n-placeholder et data-i18n-title.
   */
  function applyAll() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.textContent = t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = t(el.dataset.i18nTitle);
    });
  }

  /** Liste des langues disponibles. */
  function availableLangs() {
    return Object.keys(LOCALES);
  }

