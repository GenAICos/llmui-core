// ── Langue active ──────────────────────────────────────────────────────────
let currentLang = localStorage.getItem("financefamille_lang") || "fr";

function t(key) {
  const dict = translations[currentLang];
  const val = dict ? dict[key] : undefined;
  return val !== undefined ? val : (translations.fr[key] !== undefined ? translations.fr[key] : key);
}

function setLang(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem("financefamille_lang", lang);
  document.documentElement.lang = lang;
  applyTranslations();
  updateLangButtons();
  document.dispatchEvent(new CustomEvent("langchange", { detail: { lang } }));
}

function applyTranslations() {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    el.placeholder = t(el.getAttribute("data-i18n-placeholder"));
  });
  document.querySelectorAll("[data-i18n-title]").forEach(el => {
    el.title = t(el.getAttribute("data-i18n-title"));
  });
}

function updateLangButtons() {
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.classList.toggle("active", btn.dataset.lang === currentLang);
  });
  // Mettre à jour le label du bouton principal si dropdown
  const langLabel = document.getElementById("lang-current");
  if (langLabel) {
    const flags = { fr:"🇫🇷", en:"🇬🇧", es:"🇪🇸", de:"🇩🇪", pt:"🇧🇷", hi:"🇮🇳" };
    langLabel.textContent = (flags[currentLang] || "") + " " + currentLang.toUpperCase();
  }
}

// ── Initialisation ─────────────────────────────────────────────────────────
document.addEventListener("DOMContentLoaded", () => {
  document.documentElement.lang = currentLang;
  applyTranslations();
  updateLangButtons();

  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.addEventListener("click", () => setLang(btn.dataset.lang));
  });
});
